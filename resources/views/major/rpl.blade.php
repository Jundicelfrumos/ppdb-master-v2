            <P>Pengembangan ekosistem bisnis <strong>rintisan</strong> berbasis teknologi <i>(start-up)</i> di Indonesia membutuhkan dukungan sumber daya manusia (SDM) yang memadai dan mumpuni. Apalagi, Indonesia dinilai cukup lambat dalam pengembangan dan menangkap potensi pertumbuhan <i>start-up</i> yang diharapkan menjadi tulang punggung ekonomi masa depan  (laporan Berkarya Institute, bekerja sama dengan Natio Cultus). Laporan dua lembaga tersebut menyoroti potensi ekosistem <i>start up</i> yang sedang dibangun di Indonesia dan <strong>mengidentifikasi bakat sebagai sumber kunci</strong> yang membutuhkan perhatian segera. Indonesia pun dinilainya sudah terlambat untuk mengikuti tren <i>(bandwagon)</i> bisnis global. Sebab, ekosistem <i>start up</i> telah mengalami percepatan dan berada di garda depan dalam hal adopsi digital, penetrasi internet, <i>ecommerce, fintech. </i>dan lainnya</P>
            <br>
            <p>Disisi lain berdasarkan data BPS per Februari 2023, Sekolah Menengah Kejuruan (SMK) tercatat menghasilkan Tingkat Pengangguran Terbuka (TPT) terbanyak di Indonesia. Pengangguran dari lulusan SMK tercatat sebanyak 9,60 persen per Februari 2023. Jumlah ini turun signifikan dibandingkan data Februari 2022 yang sebesar 10,38 persen dan 2021 sebesar 11,45 persen.</p>
            <br>
            <p>Tingkat Kebekerjaan dan Pengangguran Lulusan SMK berdasarkan Bidang Keahlian berdasarkan data Sakernas (BPS) 2019, diolah oleh Puslitjak Kemendikbudristek 2020, kompetensi keahlian dipilih sepuluh kompetensi dengan tiga terbesar yang memiliki penganggur terbanyak adalah kompetensi keahlian Teknik Otomotif di urutan pertama (373.442 orang), Teknik Komputer dan Informatika di urutan kedua (246.091 orang), dan Teknik Mesin di urutan ketiga (221.368 orang). </p>
            <br>
            <p>Demikian pula dengan kompetensi keahlian teknik komputer dan informatika yang merupakan bagian dari bidang keahlian Teknologi Informasi dan Komunikasi dengan jumlah lulusan terbanyak ketiga mencapai 360.997 lulusan (22,7%) dengan tingkat pengangguran terbuka terbanyak kedua.</p>
            <br>
            <p>Berkaca cari data di atas, SMK Skill Village Islamic School Jurusan <strong>Rekayasa Perangkat Lunak (RPL)</strong> di kembangkan untuk menjawab sebagian  besar dari permasalahan di atas. Jurusan RPL dikembangkan dari ekosistem Pentahelix, mengintegrasikan dalam satu kawasan terpadu aspek akademis <i>(academic)</i>, bisnis <i>(busisness)</i>, masarakat <i>(community)</i>, keluarga <i>(family)</i> dan pemerintah <i>(government)</i>. Keterpaduan 5 aspek ini di <i>Skill Village Islamic School</i> di kenal dengan Integrasi ABCFG. </p>
            <br>
            <p>Jurusan ini di dukung oleh Grup Usaha Permata Cendekia Indonesia, yang memiliki pengalaman dibidang teknologi informasi yaitu PT. Phoenix Cendekia Indonesia. </p>
            <br>
            <p>SMK <i>Skill Village Islamic School</i> mengembangkan <i>Roadmap to Excelence</i> yang tidak hanya mengajarkan siswa cara ngoding melainkan membimbing dan menargetkan siswa untuk menciptakan produk sendiri yang layak di komersilkan. Produk yang telah mereka buat menjadi <i>point plus</i> di portofolio mereka ketika mendaftar ke universitas maupun dunia kerja. Produk tersebut juga dapat siswa kembangkan dalam bisnis tersendiri menjadi <i>start up</i> IT.  Konsep pendidikan pesantren integratif dan holistik juga menjadi pembeda profil lulusan Jurusan RPL SMK <i>Skill Village Islamic School.</i></p>
